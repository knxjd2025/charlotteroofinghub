// Helper function to get all companies
export const getAllCompanies = () => companies

// Get contributor companies (local companies that support the site)
export const getContributorCompanies = () => companies.filter(c => c.isContributor)

// Get the founder company (Best Roofing Now)
export const getFounderCompany = () => companies.find(c => c.isFounder)

// Get all companies (legacy compatibility)
export const getRegularCompanies = () => companies

// Helper function to get company by slug
export const getCompanyBySlug = (slug: string) => companies.find(c => c.slug === slug)

// Helper function to search companies
export const searchCompanies = (query: string) => {
  const searchTerm = query.toLowerCase()
  return companies.filter(c =>
    c.name.toLowerCase().includes(searchTerm) ||
    c.city.toLowerCase().includes(searchTerm) ||
    c.services.some(s => s.toLowerCase().includes(searchTerm)) ||
    c.materials.some(m => m.toLowerCase().includes(searchTerm))
  )
}
